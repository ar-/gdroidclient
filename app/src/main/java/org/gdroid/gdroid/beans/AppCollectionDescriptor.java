package org.gdroid.gdroid.beans;

public class AppCollectionDescriptor {
    private String name;

    public AppCollectionDescriptor() {
    }

    public AppCollectionDescriptor(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}